// 将树形数据转为扁平数组
function flattenTree(tree, parentLink) {
    const result = [];
    tree.forEach(node => {
        if (node.link !== parentLink) {
            result.push(node.link);
        }
        if (node.items && node.items.length > 0) {
            const items = flattenTree(node.items, node.link);
            result.push(...items);
        }
    });
    return result;
}
